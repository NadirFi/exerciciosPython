n1 = int(input('Digite um numero: '))
print('O antecessor do numero {} é: {}'.format(n1, n1-1))
print('O sucessor do numero {} é: {}'.format(n1, n1+1))
print('O dobro do numero {} é: {}'.format(n1, n1*2))
print('O triplo do numero {} é: {}'.format(n1, n1*3))
print('A raiz Quadrada do numero {} é: {:.3f}'.format(n1, n1**(1/2)))


